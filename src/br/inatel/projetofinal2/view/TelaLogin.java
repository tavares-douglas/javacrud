    public void Confirmar() {

        MedicoDAO mDAO = new MedicoDAO();
        boolean loginCorreto = mDAO.buscarLoginMedico(campoCrmMedico.getText(), campoSenhaMedico.getText());
        if ("".equals(campoSenhaMedico.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha os campos corretamente!");
        } else if (loginCorreto) {

            CadastroConsulta cc = new CadastroConsulta();
            cc.setVisible(true);
            this.dispose();

        }else
            JOptionPane.showMessageDialog(null, "Preencha os campos corretamente!");
        
    }

